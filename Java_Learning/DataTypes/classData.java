class classData {

    public static void main(String[] args) {
        // 32-bit signed integer (4 bytes)
        int num = 9;

        // 8-bit signed integer (1 byte)
        byte by = 127;

        // 16-bit signed integer (2 bytes)
        short sh = 558;

        // 64-bit signed integer (8 bytes)
        long l = 5698L;

        // 32-bit IEEE 754 floating point (4 bytes)
        float f = 5.8f;

        // 64-bit IEEE 754 floating point (8 bytes)
        double d = 5.8;

        // 16-bit Unicode character (2 bytes)
        char c = '8'; // Literals are basically values

        // Size is JVM dependent (typically 1 byte or more)
        boolean b = true;

        System.err.println(); // Just a placeholder, no output
    }
}
